    private void carregaComponentes() {
        grupoTecnicoControle.listar();
        cbGrupo.removeAllItems();
        for (GrupoTecnico grupoTecnico : grupoTecnicoControle.getGrupoTecnicos()) {
            cbGrupo.addItem(grupoTecnico.getGru_descricao());
        }
        
    }
